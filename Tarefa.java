package OperaçõesBasicas;

public class Tarefa {
	// atributo
	private String descricao;

	public Tarefa(String descricao) {
		this.descricao = descricao;
	}
	

	@Override
	public String toString() {
		return "Tarefa [descricao=" + descricao + "]";
	}


	public String getdescricao() {
		return descricao;
	}
	
	

	
	
	
	
	 
	 

}
